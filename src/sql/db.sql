CREATE TABLE IF NOT EXISTS dbo.stores (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    owner TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS dbo.users (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    lastName BOOLEAN,
    email TEXT NOT NULL,
    password TEXT NOT NULL,
    role TEXT NOT NULL
)